    public TypeAutomata() throws FileNotFoundException, IOException {
        initComponents();
        BufferedReader buf = new BufferedReader(new FileReader("file.txt"));
        ArrayList<String> List = new ArrayList<>();
        String line = buf.readLine();
        while(line != null){
            List.add(line);
            line = buf.readLine();
        }
        close.setContentAreaFilled(false);
        this.dlm = new DefaultListModel<>();
        for(String data : List){
            dlm.addElement(data);
        }
        this.dlmWrong = new DefaultListModel<>();
        this.wrongList.setModel(dlmWrong);
        answerBox.setBorder(javax.swing.BorderFactory.createEmptyBorder());
        answerBox.setBackground(Color.GRAY);
        answerBox.setEditable(false);
    }
    private void addItem(String namaItem) {
        dlmWrong.addElement(namaItem);
    }

