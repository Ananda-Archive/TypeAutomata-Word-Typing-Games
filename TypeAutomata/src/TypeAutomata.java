    private void random(){
            Random rand = new Random();
            word.setText(dlm.get(rand.nextInt(dlm.size())));
            answerBox.setText("");
    }
    private void startButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_startButtonActionPerformed
        if(startButton.getText().equals("Start!")){
            start = 1;
            counter.scheduleAtFixedRate(task, 1000, 1000);
            startButton.setText("Working...");
            startButton.setEnabled(false);
            answerBox.setBackground(Color.WHITE);
            answerBox.setEditable(true);
            random();
        }
    }//GEN-LAST:event_startButtonActionPerformed

    private void answerBoxKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_answerBoxKeyPressed
        if(evt.getKeyCode() == KeyEvent.VK_ENTER){
            if(count != 0 && start == 1){
                if(word.getText().equals(answerBox.getText())){
                    i++;
                    score.setText(Integer.toString(i));
                    random();
                }else{
                    j++;
                    wrongWords.setText("Wrong Words: " + Integer.toString(j));
                    addItem(answerBox.getText()+" - "+word.getText());
                    answerBox.setText("");
                    random();
                }
            }
        }
    }//GEN-LAST:event_answerBoxKeyPressed

